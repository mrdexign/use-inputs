export const regex = {
	phone: /^(\+)?[(]?[0-9]{3}[)]?[-\s.]?[0-9]{3}[-\s.]?[0-9]{4,6}$/,
	email: /^(([^<>()\[\]\.,;:\s@\"]+(\.[^<>()\[\]\.,;:\s@\"]+)*)|(\".+\"))@(([^<>()[\]\.,;:\s@\"]+\.)+[^<>()[\]\.,;:\s@\"]{2,})$/i,
};

export const validCharsRegex = {
	alphabet: /^[a-zA-Z\s]*$/,
	'+number': /^[0-9]*[.]?[0-9]*$/,
	number: /^(\-|\+)?[0-9]*[.]?[0-9]*$/,

	'A-Z': /^[A-Z\s]*$/,
	'a-z': /^[a-z\s]*$/,
};
